#pragma once

#include <Arduino.h>

// -----------------------------------------------------------------------------
// Типы событий энкодера
// -----------------------------------------------------------------------------
enum class EncoderEventType {
    None,

    TurnLeft,
    TurnRight,

    ButtonShort,
    ButtonLong,
    ButtonDouble
};

// -----------------------------------------------------------------------------
// Событие энкодера
// -----------------------------------------------------------------------------
struct EncoderEvent {
    int encoderId;
    EncoderEventType type;
};

// -----------------------------------------------------------------------------
// Callback
// -----------------------------------------------------------------------------
using EncoderCallback = void (*)(const EncoderEvent& event);

// -----------------------------------------------------------------------------
// Класс энкодера
// -----------------------------------------------------------------------------
class Encoder {
public:
    /**
     * @brief Создать энкодер
     *
     * @param id номер энкодера
     * @param pinA вывод A
     * @param pinB вывод B
     * @param pinBtn вывод кнопки
     * @param reverse инверсия направления вращения
     */
    Encoder(
        int id,
        int pinA,
        int pinB,
        int pinBtn,
        bool reverse = false);

    /**
     * @brief Вызывать в основном цикле
     */
    void update();

    /**
     * @brief Установить callback
     */
    void setCallback(EncoderCallback callback);

private:
    // -------------------------------------------------------------------------
    // Конфигурация
    // -------------------------------------------------------------------------
    int  _id;
    int  _pinA;
    int  _pinB;
    int  _pinBtn;
    bool _reverse;

    // -------------------------------------------------------------------------
    // Callback
    // -------------------------------------------------------------------------
    EncoderCallback _callback = nullptr;

    // -------------------------------------------------------------------------
    // Квадратурный декодер
    // -------------------------------------------------------------------------
    uint8_t _oldAB = 0;
    int8_t  _delta = 0;

    // -------------------------------------------------------------------------
    // Состояние кнопки
    // -------------------------------------------------------------------------
    bool _buttonPressed = false;
    bool _longPressSent = false;

    uint32_t _pressStartTime = 0;
    uint32_t _lastReleaseTime = 0;

    uint8_t _clickCount = 0;

private:
    /**
     * @brief Обработка вращения
     */
    void handleRotation();

    /**
     * @brief Обработка кнопки
     */
    void handleButton();

    /**
     * @brief Отправить событие
     */
    void emitEvent(EncoderEventType type);
};

