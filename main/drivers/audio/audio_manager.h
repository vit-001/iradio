#pragma once

#include "Audio.h"
#include "audio_types.h"

/**
 * @brief Центральный менеджер аудиоподсистемы.
 *
 * Отвечает за:
 * - работу с библиотекой ESP32-audioI2S;
 * - управление воспроизведением потоков;
 * - контроль состояния воспроизведения;
 * - мониторинг Wi-Fi соединения;
 * - контроль состояния входного аудиобуфера;
 * - отправку событий в UI.
 *
 * Используется как Singleton.
 */
class AudioManager {
public:

    /**
     * @brief Получить экземпляр AudioManager.
     */
    static AudioManager& getInstance();

    // ------------------------------------------------------------------------
    // Инициализация
    // ------------------------------------------------------------------------

    /**
     * @brief Инициализация I2S выхода.
     *
     * @param bckPin  BCLK
     * @param lrcPin  LRCLK / WS
     * @param doutPin DATA OUT
     */
    void begin(int bckPin, int lrcPin, int doutPin);

    /**
     * @brief Основной цикл аудиоменеджера.
     *
     * Вызывается из AudioTask.
     * Выполняет:
     * - audio.loop();
     * - контроль Wi-Fi;
     * - контроль аудиобуфера;
     * - обработку state machine.
     */
    void loop();

    // ------------------------------------------------------------------------
    // Wi-Fi
    // ------------------------------------------------------------------------

    /**
     * @brief Сохранить параметры Wi-Fi.
     */
    void setWiFiCredentials(const char* ssid, const char* password);

    /**
     * @brief Подключиться к Wi-Fi и дождаться соединения.
     *
     * @return true если подключение успешно.
     */
    bool connectWiFi();

    // ------------------------------------------------------------------------
    // Управление громкостью
    // ------------------------------------------------------------------------

    /**
     * @brief Установить громкость.
     */
    void setVolume(int vol);

    /**
     * @brief Получить текущую громкость.
     */
    int getVolume() const { return _volume; }

    /**
     * @brief Увеличить громкость на один шаг.
     */
    void volumeUp();

    /**
     * @brief Уменьшить громкость на один шаг.
     */
    void volumeDown();

    /**
     * @brief Установить стартовую громкость.
     */
    void setDefaultVolume(int vol);

    // ------------------------------------------------------------------------
    // Управление воспроизведением
    // ------------------------------------------------------------------------

    /**
     * @brief Возобновить воспроизведение.
     */
    void play();

    /**
     * @brief Поставить воспроизведение на паузу.
     */
    void pause();

    /**
     * @brief Переключение Play/Pause.
     */
    void playPause();

    /**
     * @brief Проверка факта воспроизведения.
     */
    bool isPlaying() const { return _isPlaying; }

    /**
     * @brief Подключиться к интернет-потоку.
     */
    void connectToStream(const char* url);

    /**
     * @brief Настройка эквалайзера.
     */
    void setTone(int8_t bass, int8_t mid, int8_t treble);

    // ------------------------------------------------------------------------
    // Состояние воспроизведения
    // ------------------------------------------------------------------------

    /**
     * @brief Получить текущее состояние плеера.
     */
    PlaybackState getState() const;

    /**
     * @brief Изменить текущее состояние.
     *
     * При изменении автоматически отправляет
     * уведомление в UI.
     */
    void setState(PlaybackState state);

    // ------------------------------------------------------------------------
    // Отладка
    // ------------------------------------------------------------------------

    /**
     * @brief Вывести информацию о входном буфере в лог.
     */
    void inBufferStatus();

private:

    // ------------------------------------------------------------------------
    // Singleton
    // ------------------------------------------------------------------------

    AudioManager();
    ~AudioManager();

    AudioManager(const AudioManager&) = delete;
    AudioManager& operator=(const AudioManager&) = delete;

    // ------------------------------------------------------------------------
    // Внутренние методы
    // ------------------------------------------------------------------------

    /**
     * @brief Отправить состояние Wi-Fi в UI.
     */
    void sendWiFiEvent();

    /**
     * @brief Обработчик состояния Reconnecting.
     */
    void processReconnecting();

    /**
     * @brief Отправить состояние воспроизведения в UI.
     */
    void sendPlaybackStateEvent();

    // ------------------------------------------------------------------------
    // Аудиодвижок
    // ------------------------------------------------------------------------

    Audio _audio;

    // ------------------------------------------------------------------------
    // Параметры воспроизведения
    // ------------------------------------------------------------------------

    int _volume;
    bool _isPlaying;
    const char* _currentUrl;

    // ------------------------------------------------------------------------
    // Wi-Fi
    // ------------------------------------------------------------------------

    const char* _ssid = nullptr;
    const char* _password = nullptr;

    bool _wifiConnected = false;
    int _wifiRSSI = 0;

    /**
     * @brief Время последней проверки Wi-Fi.
     */
    unsigned long _lastWiFiCheck = 0;

    /**
     * @brief Интервал проверки Wi-Fi (мс).
     */
    const unsigned long WIFI_CHECK_INTERVAL = 3000;

    // ------------------------------------------------------------------------
    // Контроль аудиобуфера
    // ------------------------------------------------------------------------

    /**
     * @brief Время последней проверки буфера.
     */
    uint32_t _lastBufferCheck = 0;

    /**
     * @brief Интервал проверки буфера (мс).
     */
    const unsigned long BUFFER_CHECK_INTERVAL = 500;

    // ------------------------------------------------------------------------
    // State Machine
    // ------------------------------------------------------------------------

    /**
     * @brief Текущее состояние аудиоподсистемы.
     */
    PlaybackState currentState = PlaybackState::Idle;
};